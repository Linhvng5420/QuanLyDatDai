    private void btnBanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBanActionPerformed
        // TODO add your handling code here:
        Dat ban = new Dat();

        // Nhập:
        try {
            ban.setMaDat(Integer.parseInt(txtMaDat.getText()));
            ban.setDienTich(Integer.parseInt(txtDienTich.getText()));
            ban.setTenDat(txtTenDat.getText());
            ban.setGiaDat(Integer.parseInt(txtGia.getText()));
            ban.setViTri(txtViTri.getText());
            ban.setTrangThai("Chua Ban");
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(this, "Nhập Lỗi Thiếu Hoặc Sai!", "", JOptionPane.CLOSED_OPTION, JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        // Trả KQ:
        if (banControl.ThemDL(ban) == true) {
            LoadTableBan();
            JOptionPane.showConfirmDialog(this, "Bán Thành Công!", "", JOptionPane.CLOSED_OPTION, JOptionPane.INFORMATION_MESSAGE);

        } else {
            JOptionPane.showConfirmDialog(this, "Bán Thất Bại!", "", JOptionPane.CLOSED_OPTION, JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnBanActionPerformed

    @SuppressWarnings("empty-statement")
    private void btnHuyBanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHuyBanActionPerformed
        // TODO add your handling code here:
        if (JOptionPane.showConfirmDialog(this, "Bạn muốn huỷ bán lô đất này?", "", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == 0) {
            if (banControl.HuyBan(flagMaDatBan) == true) {
                banControl.XoaDatdaBan(flagMaDatBan);
                JOptionPane.showConfirmDialog(this, "Huỷ Bán Thành Công!", "", JOptionPane.CLOSED_OPTION, JOptionPane.INFORMATION_MESSAGE);
                // Làm mới table
                LoadTableBan();
            } else {
                JOptionPane.showConfirmDialog(this, "Huỷ Bán Thất Bại!", "", JOptionPane.CLOSED_OPTION, JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnHuyBanActionPerformed

