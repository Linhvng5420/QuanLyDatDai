    public void LoadTableBan() {
        DefaultTableModel ban = (DefaultTableModel) jTableBan.getModel();
        ban.setRowCount(0);

        for (Dat banModel : banControl.getDsDat()) {
            Object[] obj = {banModel.getMaDat(), banModel.getTenDat(), banModel.getDienTich(), banModel.getGiaDat(), banModel.getViTri()};
            ban.addRow(obj);
        }
    }

