    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // Nhập:
        try {
            dat.setMaDat(Integer.parseInt(txtMaDat.getText()));
            dat.setTenDat(txtTenDat.getText());
            dat.setDienTich(Integer.parseInt(txtDienTich.getText()));
            dat.setGiaDat(Integer.parseInt(txtGiaDat.getText()));
            dat.setViTri(txtViTri.getText());
            dat.setTrangThai((String) jComboBox.getSelectedItem());
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(this, "Nhập Dữ Liệu Sai!", "", JOptionPane.CLOSED_OPTION, JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        // Trả KQ:
        if (qtrControl.ThemDat(dat) == true) {
            JOptionPane.showConfirmDialog(this, "Thêm Thành Công!", "", JOptionPane.CLOSED_OPTION, JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showConfirmDialog(this, "Thêm Thất Bại, Mã Đất Đã Tồn Tại!", "", JOptionPane.CLOSED_OPTION, JOptionPane.INFORMATION_MESSAGE);
        }
        try {
            LoadTableDat();
        } catch (SQLException ex) {
            Logger.getLogger(FrQuanTri.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed

        if (JOptionPane.showConfirmDialog(this, "Bạn muốn xoá lô đất này?", "", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == 0) {
            if (qtrControl.XoaDat(clickMaDat) == true) {
                JOptionPane.showConfirmDialog(this, "Xoá Thành Công!", "", JOptionPane.CLOSED_OPTION, JOptionPane.INFORMATION_MESSAGE);
                try {
                    // Làm mới table
                    LoadTableDat();
                } catch (SQLException ex) {
                    Logger.getLogger(FrQuanTri.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showConfirmDialog(this, "Xoá Thất Bại!", "", JOptionPane.CLOSED_OPTION, JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed


