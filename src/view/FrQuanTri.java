    // Load dữ liệu cho table từ CSDL
    private void LoadTableDat() throws SQLException {
        DefaultTableModel ban = (DefaultTableModel) tableDat.getModel();
        ban.setRowCount(0);

        for (Dat dat : qtrControl.GetTable()) {
            Object[] obj = {dat.getMaDat(), dat.getTenDat(), dat.getDienTich(), dat.getGiaDat(), dat.getViTri(), dat.getTrangThai()};
            ban.addRow(obj);
        }
    }

