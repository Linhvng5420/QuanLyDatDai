    private void loadTableMua() throws SQLException {

        DefaultTableModel mua = (DefaultTableModel) jTableMua.getModel();
        mua.setRowCount(0);

        for (Dat muaModel : muaControl.GetTableMua()) {
            Object[] obj = {muaModel.getMaDat(), muaModel.getTenDat(), muaModel.getDienTich(), muaModel.getGiaDat(), muaModel.getViTri(), muaModel.getTrangThai()};
            mua.addRow(obj);
        }
    }

    private void loadTableDaMua() throws SQLException {

        DefaultTableModel daMua = (DefaultTableModel) jTableDaMua.getModel();
        daMua.setRowCount(0);

        for (Dat item : muaControl.GetTableDaMua()) {
            Object[] obj = {item.getMaDat(), item.getTenDat(), item.getDienTich(), item.getGiaDat(), item.getViTri(), item.getTrangThai()};
            daMua.addRow(obj);
        }
    }

