    private void jTableMuaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableMuaMouseClicked
        // TODO add your handling code here:
        int x = jTableMua.getSelectedRow();
        Dat dat = muaControl.GetMuaByIndex(x);

        //Lưu maDat của đối tượng, dùng nó để truy vấn đến đối tượng trong danh sách ArrayList
        flagMaDatMua = dat.getMaDat();
    }//GEN-LAST:event_jTableMuaMouseClicked

// Click chọn, lưu flagMaDat
// Nhấn button FrMua, update dl cho đối tượng có maDat = flagMaDat thành "Da Ban" 
    private void jButtonMuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonMuaActionPerformed
        // TODO add your handling code here:
        if (JOptionPane.showConfirmDialog(this, "Bạn muốn mua lô đất này?", "", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == 0) {
            try {
                muaControl.UpdateCSDL(flagMaDatMua, "Da Ban");
                JOptionPane.showConfirmDialog(this, "Mua Thành Công!", "", JOptionPane.CLOSED_OPTION, JOptionPane.INFORMATION_MESSAGE);
                muaControl.ThemDatDaMua(flagMaDatMua);
                
            } catch (SQLException ex) {
                JOptionPane.showConfirmDialog(this, "Lỗi Mua!", "", JOptionPane.CLOSED_OPTION, JOptionPane.WARNING_MESSAGE);
            }
        }
        
        // Làm mới table:
        try {
            loadTableMua();
            loadTableDaMua();
        } catch (SQLException ex) {
            Logger.getLogger(FrMua.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonMuaActionPerformed
