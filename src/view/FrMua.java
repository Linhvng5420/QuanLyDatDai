    //Nhấn Huy FrMua, update dl cho đối tượng có maDat = flagMaDat thành "Da Ban" 
    private void jButtonHuyMuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonHuyMuaActionPerformed
        // TODO add your handling code here:
        if (JOptionPane.showConfirmDialog(this, "Bạn muốn huỷ mua lô đất này?", "", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == 0) {
            try {
                muaControl.UpdateCSDL(flagMaDatDaMua, "Chua Ban");
                muaControl.XoaDatDaMua(flagMaDatDaMua);
                JOptionPane.showConfirmDialog(this, "Huỷ Mua Thành Công!", "", JOptionPane.CLOSED_OPTION, JOptionPane.INFORMATION_MESSAGE);
            } catch (SQLException ex) {
                JOptionPane.showConfirmDialog(this, "Lỗi Huỷ Mua!", "", JOptionPane.CLOSED_OPTION, JOptionPane.WARNING_MESSAGE);
            }
        }

        // Làm mới table
        try {
            loadTableDaMua();
            loadTableMua();
        } catch (SQLException ex) {
            Logger.getLogger(FrMua.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonHuyMuaActionPerformed

